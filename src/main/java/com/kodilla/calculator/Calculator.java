package com.kodilla.calculator;

public class Calculator {


    public void addMethod(double firstNumber, double secondNumber) {
        System.out.println(firstNumber + " + " + secondNumber +" = "+ (firstNumber + secondNumber));
    }

    public void substractMethod(double firstNumber, double secondNumber) {
        System.out.println(firstNumber + " - " + secondNumber +" = "+ (firstNumber - secondNumber));
    }
}
